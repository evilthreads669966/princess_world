@OptIn(ExperimentalPagerApi::class)
@Composable
fun ViewPhoto(selectedPhoto: SelectedPhoto){
    val state = rememberPagerState()
    Box {
        HorizontalPager(count = selectedPhoto.photos.size, state = state, modifier = Modifier.height(800.dp)) { page ->
            AsyncImage(model = selectedPhoto.photos[page], contentDescription = null, modifier = Modifier
                .fillMaxSize()
                .align(Alignment.Center)
                .padding(horizontal = 4.dp))
            LaunchedEffect(null){
                state.scrollToPage(selectedPhoto.index)
            }
        }
        HorizontalPagerIndicator(pagerState = state, modifier = Modifier
            .zIndex(15f)
            .padding(bottom = 16.dp)
            .align(Alignment.BottomCenter), indicatorWidth = 10.dp, indicatorHeight = 10.dp, indicatorShape = CircleShape
        )
    }
}
