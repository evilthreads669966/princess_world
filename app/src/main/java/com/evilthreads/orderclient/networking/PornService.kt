interface PornService{
    companion object{
        private val BASE_URL = "https://www.eporner.com/api/v2/"

        fun create(): PornService{
            val retrofit = Retrofit.Builder().baseUrl(BASE_URL).addConverterFactory(
                GsonConverterFactory.create()).build()
            return retrofit.create(PornService::class.java)
        }
    }
    @GET("video/search/?query=femdom&per_page=1000")
    fun getVideos(): Call<VideoResponse>
}