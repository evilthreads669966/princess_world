class PrincessesTest {
    @get :Rule
    val rule = createComposeRule()
    val viewmodel = PrincessViewModel()

    @Test
    fun princesses_exist_and_are_clickable(){
        rule.setContent {
            AppScaffold(viewmodel)
        }
        val child = rule.onNodeWithTag("princess_list").onChildren().get(0).assertExists()
        child.assertIsDisplayed().assertHasClickAction()
        
    }
}